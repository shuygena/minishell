#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char			i;
	unsigned int	V;

	if (n < 0)
	{
		V = n * -1;
		write(fd, "-", 1);
	}
	else
		V = n;
	if (V >= 10)
		ft_putnbr_fd(V / 10, fd);
	i = (V % 10) + '0';
	write(fd, &i, 1);
}
